package com.net.requests;

import com.net.base.Request;
import com.net.base.Response;

import org.json.JSONException;
import org.json.JSONObject;

import java.util.Map;

public class JsonRequest extends Request<JSONObject> {

    public JsonRequest(HttpMethod method, String url, Map<String, String> params,
            com.net.base.Request.RequestListener<JSONObject> listener) {
        super(method, url, params, listener);
    }

    @Override
    public JSONObject parseResponse(Response response) {
        String jsonString = new String(response.getRawData());
        try {
            return new JSONObject(jsonString);
        } catch (JSONException e) {
            e.printStackTrace();
        }
        return null;
    }
}
